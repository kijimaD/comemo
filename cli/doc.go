// Package cli は comemo アプリケーションのコマンドラインインターフェース機能を提供します。
//
// このパッケージの責務:
// - urfave/cli/v3を使用したCLIコマンドとフラグの定義
// - コマンドライン引数の解析と検証
// - 内部パッケージ間の連携によるユーザーコマンドの実行
// - 全てのアプリケーション操作に対するユーザーフレンドリーなインターフェースの提供
//
// CreateApp() がメインエントリーポイントで、以下の全コマンドを含む完全なCLIアプリケーションを作成します:
//
//   - collect: Goリポジトリからコミットデータを収集
//   - generate: 不足している説明用のプロンプトスクリプトを生成
//   - execute: プロンプトスクリプトを実行して説明を生成
//   - verify: 生成されたファイルの整合性を検証
//   - all: 全ステップを順次実行
//   - status: 現在の状態と統計情報を表示
//
// 各コマンドは実際の作業を適切な内部パッケージに委譲し、
// 設定の更新やエラーハンドリングなどの共通関心事を処理します。
