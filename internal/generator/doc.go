// Package generator は コミット説明生成用のプロンプトスクリプトを生成する機能を提供します。
//
// このパッケージの責務:
// - 収集されたコミットデータに基づくプロンプトスクリプトの生成
// - AI CLIツール実行用のシェルスクリプトの作成
// - 適切なプロンプトテンプレートの適用
// - 既存の説明ファイルの重複チェック
// - 並行処理による効率的なスクリプト生成
//
// 主な機能:
//
// GeneratePrompts() - commit_dataディレクトリの各コミットファイルに対して
// プロンプトスクリプトを生成します。各スクリプトは構造化された指示を含み、
// AI CLIツールに詳細なコミット説明の生成を依頼します。
//
// 生成されるプロンプトには以下の要素が含まれます:
// - コミットメタデータ（インデックス、ハッシュ、GitHub URL）
// - 構造化された章構成の指示
// - 技術的詳細の要求事項
// - 日本語での詳細解説の指示
//
// 各スクリプトは実行可能な状態で保存され、
// executorパッケージによって後から実行されます。
// 既に説明が存在するコミットについてはスクリプト生成をスキップし、
// 効率的な処理を実現します。
