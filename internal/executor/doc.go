// Package executor は AI CLIツールを使用してプロンプトスクリプトを実行する機能を提供します。
//
// このパッケージの責務:
// - 複数のAI CLIツール（Claude、Gemini等）の管理
// - プロンプトスクリプトの並行実行
// - クォータ制限の検出と適切な再試行処理
// - 実行進捗の監視と報告
// - 失敗したスクリプトの再実行制御
//
// 主な機能:
//
// ExecutePrompts() - 生成されたプロンプトスクリプトを指定されたAI CLIで実行し、
// コミット説明を生成します。複数のAI CLIツールを並行して使用することで
// 処理効率を向上させ、クォータ制限を回避します。
//
// CLIManager - 各AI CLIツールの状態（利用可能性、クォータ状態）を管理し、
// 適切なロードバランシングを行います。
//
// Worker - 個別のAI CLIツールでスクリプトを実行するワーカープロセス。
// エラーハンドリング、リトライ制御、結果の保存を担当します。
//
// このパッケージは、AI APIの制限に対して耐性があり、
// 大量のコミット説明を効率的に生成できるよう設計されています。
package executor