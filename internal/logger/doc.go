// Package logger は 構造化されたログ機能を提供します。
//
// このパッケージの責務:
// - レベル別ログ出力の管理（DEBUG, INFO, WARN, ERROR, SILENT）
// - 異なる出力先への書き分け（標準出力/標準エラー出力）
// - タイムスタンプ付きの構造化ログメッセージ
// - ログレベルの動的制御
// - アプリケーション全体での一貫したログ形式
//
// 主な機能:
//
// Logger構造体 - 設定可能なログレベルと出力先を持つロガーインスタンス。
// 各ログレベルに対応したメソッド（Debug, Info, Warn, Error）を提供します。
//
// ログレベル制御 - 設定されたレベル以下のログのみが出力され、
// 本番環境では詳細なデバッグ情報を抑制できます。
//
// 複数出力形式 - タイムスタンプ付きの構造化ログと、
// プレーンテキストのログ出力を選択できます。
//
// Default() とSilent() - よく使用される設定のための便利コンストラクタ。
// テスト環境や本番環境での適切なログ設定を簡単に作成できます。
//
// このパッケージは、デバッグ、監視、トラブルシューティングに必要な
// 包括的なログ機能を提供し、アプリケーションの運用性を向上させます。
package logger