// Package verifier は 生成されたファイルの整合性検証機能を提供します。
//
// このパッケージの責務:
// - 各処理段階でのファイル数の整合性チェック
// - プロジェクト全体の処理進捗状況の把握
// - データの不整合や欠損の検出
// - 次に実行すべきコマンドの提案
// - 処理完了状況の可視化
//
// 主な機能:
//
// Verify() - プロジェクト全体のデータ整合性を検証し、詳細なレポートを出力します。
// 以下の要素を検証対象とします:
//
// - リポジトリ内の総コミット数
// - commit_dataディレクトリ内の収集済みデータ数
// - promptsディレクトリ内の生成済みスクリプト数
// - srcディレクトリ内の完成済み説明ファイル数
//
// 進捗レポート - 各段階の完了率を計算し、視覚的に分かりやすい
// 進捗情報を提供します。未完了の作業がある場合は、
// 次に実行すべきコマンドを具体的に提案します。
//
// 不整合検出 - ファイル数の不一致や予期しない状態を検出し、
// 問題の特定と解決に必要な情報を提供します。
//
// このパッケージは、大量のコミット処理において
// データの完全性を保証し、処理の透明性を提供する重要な役割を担います。
