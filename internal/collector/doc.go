// Package collector は Goリポジトリからコミットデータを収集する機能を提供します。
//
// このパッケージの責務:
// - Gitリポジトリからコミット情報の収集
// - コミットデータの並行処理による効率的な取得
// - 収集したデータのファイルシステムへの保存
// - 既存データの重複処理を避けるためのチェック機能
//
// 主な機能:
//
// CollectCommits() - 指定されたGoリポジトリから全コミットデータを収集し、
// commit_dataディレクトリに番号付きのテキストファイルとして保存します。
// 各コミットには一意のインデックス番号が割り当てられ、
// このインデックスが後続の処理（プロンプト生成、説明生成）で使用されます。
//
// 並行処理により大量のコミットデータを効率的に処理し、
// 既に収集済みのデータはスキップすることで不要な重複処理を避けます。
//
// 進捗状況はログレベルに応じて出力され、
// 処理の透明性と監視可能性を提供します。
package collector
