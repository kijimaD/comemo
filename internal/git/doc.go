// Package git は Gitリポジトリとの連携機能を提供します。
//
// このパッケージの責務:
// - Gitコマンドの実行とエラーハンドリング
// - コミットハッシュの取得とリスト化
// - 個別コミットの詳細データ取得
// - コミットインデックスの管理とマッピング
//
// 主な機能:
//
// RunCommand() - 指定されたディレクトリでGitコマンドを実行し、
// 標準出力と標準エラー出力を適切に処理します。
// 全てのGit操作の基盤となる関数です。
//
// GetCommitHashes() - リポジトリ内の全コミットハッシュを時系列順で取得します。
// このハッシュリストが、後続の処理でコミットの一意識別子として使用されます。
//
// GetCommitData() - 指定されたコミットハッシュの詳細情報
// （差分、統計情報、メタデータ）を取得します。
//
// GetCommitIndex() - コミットハッシュリスト内での位置（インデックス）を計算します。
// このインデックスは、ファイル名や処理順序の決定に使用されます。
//
// このパッケージは、comemoアプリケーションの中核となるGitリポジトリ操作を
// 安全かつ効率的に実行する基盤を提供します。
